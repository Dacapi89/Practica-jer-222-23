public class Message {
	private long Id;
	private String Content;
	public String getContent() {
		return Content;
	}
	public void setContent(String str) {
		Content = str;
	}
	public long getId() {
		return Id;
	}
	public void setId(long l) {
		Id = l;
	}
}
